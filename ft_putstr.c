#include "ft_printf.h"

void	ft_putstr(char *s, int *count)
{
	if (!s)
	{
		write(1, "(null)", 6);
		*count += 6;
		return ;
	}
	while (*s)
	{
		write(1, &*s, 1);
		*count += 1;
		s++;
	}
}
